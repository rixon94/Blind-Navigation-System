uint8_t LackOfObject(uint32_t time){

	uint32_t clk_Start = HAL_GetTick();

	if(clk_Start)
	do {
		//if the object in in the area radius, go out
		if (sensorGet(0) <= 400 || sensorGet(1) <= 400 || sensorGet(2) <= 400)
		{
			return 0;
		}

	}while (time < HAL_GetTick() - clk_Start);

	return 1;
}
